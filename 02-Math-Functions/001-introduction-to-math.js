
//!            Topic: Introduction to Math in JavaScript

/*
 In JavaScript, 'Math' is a built-in object that helps us perform mathematical operations easily.
It provides methods and constants like:
 Math.round(), Math.floor(), Math.ceil(), Math.random(), Math.abs(), etc.

 You don’t need to create a Math object — you can directly use Math.methodName()
*/

// ------------------------------
//   Example 1: Math is an Object
// ------------------------------
console.log(" Math Object Example:");
console.log(Math); // prints the Math object (collection of all math methods)


// ------------------------------
// Example 2 : Math Constants
// ------------------------------
console.log("Math Constants:");
console.log("PI value:", Math.PI);   // π = 3.141592653589793
console.log("E value:", Math.E);     // Euler's number = 2.718281828459045


// ------------------------------
//  Summary
// ------------------------------
/*
 Math is a global object — you don’t need to import it.
 It provides constants and methods for math operations.
*/

